package ch.tbe;

public class Attribute {
 
	private String text;
	private String title;
	 
	public void Attribute(String text, String title) {
		this.text = text;
		this.title = title;
	}
	 
	public void setText(String text) {
		this.text = text;
	}
	 
	public void setTitle(String title) {
		this.title = title;
	}
}
 
